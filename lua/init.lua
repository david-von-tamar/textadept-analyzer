local analyzer = {}
--------------------------------------------------------------------------------
analyzer.command = "luacheck --codes --ranges --no-color"
--------------------------------------------------------------------------------
function analyzer.parse_issue(line)
	local line_index, from, to, prefix, message =
		line:match(".+:(%d+):(%d+)%-(%d+): %(([EW])%d+%) (.+)$")
	if prefix == "E" then
		 return line_index, from, to, 1, message
	elseif prefix == "W" then
		 return line_index, from, to, 2, message
	end
end
--------------------------------------------------------------------------------
return analyzer
